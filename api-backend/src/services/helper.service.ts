export class HelperService {
    static createOutputFilePath(): string {
        let date = new Date();
        let dateString = date.getFullYear() + "-" + ("0" + (date.getMonth() + 1)).slice(-2) + "-" +
            ("0" + date.getDate()).slice(-2) + "T" + ("0" + date.getHours()).slice(-2) + "-" +
            ("0" + date.getMinutes()).slice(-2) + "-" + ("0" + date.getSeconds()).slice(-2);
        return ("output/CreatePDFFromHTMLWithInlineCSS/create" + dateString + ".pdf");
    }

    static createDecryptOutputFilePath(): string {
        let date = new Date();
        let dateString = date.getFullYear() + "-" + ("0" + (date.getMonth() + 1)).slice(-2) + "-" +
            ("0" + date.getDate()).slice(-2) + "T" + ("0" + date.getHours()).slice(-2) + "-" +
            ("0" + date.getMinutes()).slice(-2) + "-" + ("0" + date.getSeconds()).slice(-2);
        return ("output/Decypted/unprotected" + dateString + ".pdf");
    }

    static CreateHtmlOutputFilePath(prefix: string, fileExtension: string | null = null) {
        let date = new Date();
        let dateString = date.getFullYear() + "-" + ("0" + (date.getMonth() + 1)).slice(-2) + "-" +
            ("0" + date.getDate()).slice(-2) + "T" + ("0" + date.getHours()).slice(-2) + "-" +
            ("0" + date.getMinutes()).slice(-2) + "-" + ("0" + date.getSeconds()).slice(-2);

        if (fileExtension != null)
            return ("output/PdfToHtml/"+prefix+"_"+dateString+fileExtension)
        else
            return ("output/PdfToHtml/"+prefix)
    }
}